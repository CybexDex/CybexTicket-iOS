import UIKit

extension UIColor {
    
    @nonobjc class var steel: UIColor {
        return UIColor(red: 120.0 / 255.0, green: 129.0 / 255.0, blue: 154.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var yellow: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 123.0 / 255.0, blue: 70.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var darkTwo: UIColor {
        return UIColor(red: 27.0 / 255.0, green: 34.0 / 255.0, blue: 48.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var paleGrey: UIColor {
        return UIColor(red: 247.0 / 255.0, green: 248.0 / 255.0, blue: 250.0 / 255.0, alpha: 1.0)
    }
    
}
